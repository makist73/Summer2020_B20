package day03_sequences_Variables;

public class Diamond {

    public static void main(String[] args) {
        System.out.println("     ^");
        System.out.println("    / \\");
        System.out.println("   / * \\");
        System.out.println("  / * * \\");
        System.out.println(" / * * * \\");
        System.out.println(" ---------");
        System.out.println(" \\ * * * /");
        System.out.println("  \\ * * /");
        System.out.println("   \\ * /");
        System.out.println("    \\ /  ");
        System.out.println("     - ");
    }
}
